        c(6)*cos(3*x) + c(7)*sin(3*x) + c(8)*cos(4*x) + c(9)*sin(4*x) + ...
        c(10)*cos(5*x) + c(11)*sin(5*x) + c(12)*cos(6*x) + c(13)*sin(6*x) + ...
